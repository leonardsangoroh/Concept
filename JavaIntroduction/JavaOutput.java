package JavaIntroduction;

public class JavaOutput {
    
    public static void main(String[]args) {

        //Prints string inside quotes 
        System.out.print("Lee");

        //Same as print but moves cursor to the next line
        System.out.println("Lee");

        //An overloaded java method. Gives output depending on arguments passed
        System.out.printf("lee");
    }
}
